public class ModelFondos {
    private String clvObra, clvObraNo, desc, tipo;
    private int id;
    private float tot, fed, est, mpal, bene, otr;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClvObra() {
        return clvObra;
    }

    public void setClvObra(String clvObra) {
        this.clvObra = clvObra;
    }

    public String getClvObraNo() {
        return clvObraNo;
    }

    public void setClvObraNo(String clvObraNo) {
        this.clvObraNo = clvObraNo;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getTot() {
        return tot;
    }

    public void setTot(float tot) {
        this.tot = tot;
    }

    public float getFed() {
        return fed;
    }

    public void setFed(float fed) {
        this.fed = fed;
    }

    public float getEst() {
        return est;
    }

    public void setEst(float est) {
        this.est = est;
    }

    public float getMpal() {
        return mpal;
    }

    public void setMpal(float mpal) {
        this.mpal = mpal;
    }

    public float getBene() {
        return bene;
    }

    public void setBene(float bene) {
        this.bene = bene;
    }

    public float getOtr() {
        return otr;
    }

    public void setOtr(float otr) {
        this.otr = otr;
    }
    
}
